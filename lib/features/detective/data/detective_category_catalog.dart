  static DetectiveCategory? findById(String? id) {
    if (id == null || id.isEmpty) {
      return null;
    }

    for (final category in categories) {
      if (category.id == id) {
        return category;
      }
    }

    return null;
  }

  static List<String> subcategoriesFor(String? categoryId) {
    return findById(categoryId)?.subcategories ?? const [];
  }
